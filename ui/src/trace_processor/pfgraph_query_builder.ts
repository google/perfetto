export class PfgraphModule {
  private readonly moduleName: string;
  private readonly imports: string[] = [];
  private readonly declarations: string[] = [];

  constructor(moduleName: string) {
    this.moduleName = moduleName;
  }

  import(...modules: string[]): PfgraphModule {
    this.imports.push(...modules);
    return this;
  }

  addPipeline(
    name: string,
    pipeline: PfgraphPipeline,
    opts?: {type?: 'table' | 'view'; index?: string[]},
  ): PfgraphModule {
    this.declarations.push(pipeline.renderAs(name, opts));
    return this;
  }

  addFunction(decl: string): PfgraphModule {
    this.declarations.push(decl);
    return this;
  }

  render(): string {
    const lines: string[] = [];
    lines.push(`module ${this.moduleName}`);
    lines.push('');
    for (const imp of this.imports) {
      lines.push(`import ${imp}`);
    }
    if (this.imports.length > 0) {
      lines.push('');
    }
    lines.push(this.declarations.join('\n\n'));
    return lines.join('\n');
  }
}
